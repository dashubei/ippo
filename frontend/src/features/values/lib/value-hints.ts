// 価値が「ゴール」「感情/症状の低減」「他者の期待（pliance）」と混同されがちな入力を、
// ブロックせずにやさしく問い返すためのソフト検知。保存は妨げない（あくまでヒント）。
// pliance は「ようになる」等のゴール語尾も含みやすいので、ゴールより先に判定する。
export const detectValueHint = (text: string): string | null => {
  const t = text.trim()
  if (!t) return null

  if (
    /(不安|緊張|きんちょう|怖|こわ|ストレス|どきどき|ドキドキ)(を|が)?.*(なくす|無くす|消す|減らす|なくな|感じない|しない|なおす|治す)/.test(
      t,
    ) ||
    /(平気になる|落ち着けるようになる|緊張しない)/.test(t)
  ) {
    return '「不安をなくす」こと自体より、不安があっても“どんな自分でいたいか”を書くと、次の一歩を決めやすくなります。'
  }

  if (/(好かれ|嫌われ|認められ|期待に応え|迷惑をかけ|変だと思われ)/.test(t)) {
    return '「人にどう思われるか」の奥に、あなたが大切にしたい“関わり方”があるかもしれません。それを一言にすると？'
  }

  if (/(ようになる|達成|成功する|合格|手に入れる|克服する|やめる)/.test(t)) {
    return 'それは「目標」かもしれません。その奥にある“大切にしたいこと”を一言にすると？（例: 誠実でいる／人とつながる）'
  }

  return null
}
